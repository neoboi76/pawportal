/**
 * Configuration class for EmailService and ForgotService
 * Specifies basic settings, such as email sender and app password,
 * email host, among other properties
*/

@Configuration
public class MailConfig {

    //Email sender. Environment variable. Typically stored
    //in the applications.properties file
    @Value("${spring.mail.username}")
    private String username;

    //App password for the email. Environment variable. Typically stored
    //in the applications.properties file
    @Value("${spring.mail.password}")
    private String password;

    @Bean
    public JavaMailSender javaMailSender() {

        JavaMailSenderImpl mailSender = new JavaMailSenderImpl();
        mailSender.setHost("smtp.gmail.com");
        mailSender.setPort(587);

        mailSender.setUsername(username);
        mailSender.setPassword(password);

        Properties props = mailSender.getJavaMailProperties();
        props.put("mail.transport.protocol", "smtp");
        props.put("mail.smtp.auth", "true");
        props.put("mail.smtp.starttls.enable", "true");
        props.put("mail.debug", "true");

        return mailSender;
    }
}